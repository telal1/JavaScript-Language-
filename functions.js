// Defining a function
function greet(name) {
    return 'Hello, ' + name + '!';
}

// Calling the function
let greeting = greet('Telal');
console.log(greeting);



// Defining an arrow function to add two numbers
const add = (a, b) => {
    return a + b;
};

// Calling the arrow function to add 5 and 3
let result = add(5, 3);
console.log(result); 
